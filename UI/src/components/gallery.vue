<template>
    <div class="gallery">
        <imageItem v-for="(image, index) in images" v-on:click.native='clicked(index)' v-bind:imageURL="image.url"/>

        <div id="myModal" class="imageModal">
            <span class="close" id="closeImageButton">&times;</span>
            <div class='innerContent'>
                <div class="carouselButton" v-on:click="prev()">
                    <span >&#8249;</span>
                </div>
                <div id="overIBS">
                    <div class="image-modal-content">
                        <imageWBBItem id="imageBigShow" v-bind:faces="this.faces" v-bind:image="this.images[this.index]" v-bind:avatars="this.avatarsById"/>
                    </div>
                </div>
                <div class="carouselButton" v-on:click="next()">
                    <span>&#8250;</span>
                </div>
            </div>
        </div>

 
    </div>
</template>

<script>
    import imageItem from './imageItem.vue';
    import imageWBBItem from './imageWBBItem.vue';
    import axios from 'axios';

    window.onload = function() {
        var modal = document.getElementById('myModal');

        var span = document.getElementById('closeImageButton'); 

        span.onclick = function() {
            this.index = null;
            modal.style.display = "none";
        }
    }

    export default {
        name: 'gallery',
        components: {
            imageItem,
            imageWBBItem,
        },
        props: {
            images: {
              type: Array,
              default () {
                  return [];
              }
            },
            avatars: {
                type: Array,
                default () {
                    return [];
                }
            },
        },
        data() {
            return {
                index: null,
                imagenowURL: '',
                faces: {},
                avatarsById: {},
            };
        },
        watch: {
            index(value) {
                this.imagenowURL = this.images[value];
                this.getFaces();
            },
        },
        created: function () {
            document.onkeydown= this.onkeydown;
        },
        mounted: function () {
            for (var avatar in this.avatars) {
                this.avatarsById[avatar.id] = avatar;
            }
        },
        methods: {
            clicked(index) {
                this.index = index;
                var modal = document.getElementById('myModal');
                modal.style.display = "flex";
            },
            getFaces() {
                var this_ = this;
                if (!this_.images || !this_.index && this.index != 0) {
                    this_.faces = {};
                    return;
                }
                axios.get('api/faces/' + String(this_.images[this_.index].id) + '/', { headers: {Authorization: "Token " + localStorage.token}}).then(function (response) {
                        this_.faces = response.data.faces;
                }).catch(function (error) {
                    this_.faces = {};
                });
            },
            next() {
                this.index += 1;
                if (this.index == this.images.length) {
                    this.index = 0;
                }
            },
            prev() {
                this.index -= 1;
                if (this.index < 0) {
                    this.index = this.images.length - 1;
                }
            },
            close() {
                this.index = null;
                document.getElementById('myModal').style.display = "none";
            },
            onkeydown(e) {
                if (e.key == "Escape") {
                    this.close();
                }
                else if (e.key == "ArrowRight") {
                   this.next();
                }
                else if (e.key == "ArrowLeft"){
                    this.prev();
                }
            }
        }
    }
</script>

